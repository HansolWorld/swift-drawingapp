import Foundation


struct Color {
    let r: Int
    let g: Int
    let b: Int
    let alpha: Int
    
    init(r: Int, g: Int, b:Int, alpha: Int) {
        self.r = r
        self.g = g
        self.b = b
        self.alpha = alpha
    }
}
